void DrawIcon32x16(u16 xPos, u16 yPos, const void *Icon)
{
    u8 *ptrIcon, *cPnt;
    u8 xOfs, i, j;


    ptrIcon = (u8 *)Icon;

    cPnt = &GrafBff[(xPos / 8) + yPos * (YPIXELS / 8)];    //Вычисление указателя в буфере по заданным координатам
    xOfs = xPos & 7;                                          //Смещение в битах

    for(i = 0; i < 16; i++, cPnt += YPIXELS / 8, ptrIcon += 4){
        for (j = 0; j < 4; j++) {
            cPnt[j] |= ptrIcon[j] >> xOfs;
            cPnt[j + 1] |= ptrIcon[j] << (8 - xOfs);
            cPnt[j + 2] |= ptrIcon[j] << (16 - xOfs);
            cPnt[j + 3] |= ptrIcon[j] << (32 - xOfs);
        }
    } 
}
